let currentIndex = 0;
let score = 0;
let questions = [];

const questionElem = document.getElementById("question");
const choicesElem = document.getElementById("choices");
const nextBtn = document.getElementById("next-btn");
const popup = document.getElementById("popup");

function shuffleArray(arr) {
  return arr.sort(() => Math.random() - 0.5);
}

function startQuiz() {
  questions = shuffleArray([...allQuestions]).slice(0, 7);
  currentIndex = 0;
  score = 0;
  nextBtn.textContent = "次へ";
  showQuestion();
}

function showQuestion() {
  const current = questions[currentIndex];
  questionElem.textContent = current.q;
  choicesElem.innerHTML = "";

  const shuffledChoices = shuffleArray([...current.c]);
  shuffledChoices.forEach(choice => {
    const btn = document.createElement("button");
    btn.textContent = choice;
    btn.onclick = () => selectAnswer(choice);
    choicesElem.appendChild(btn);
  });

  nextBtn.classList.add("hidden");
  popup.classList.add("hidden");
}

function selectAnswer(choice) {
  const current = questions[currentIndex];
  const buttons = document.querySelectorAll("#choices button");

  buttons.forEach(btn => btn.disabled = true);

  if (choice === current.a) {
    score++;
    showPopup("✅ 正解！", "#00FF88");
  } else {
    showPopup(`❌ 不正解！ 正解は「${current.a}」`, "#FF5555");
  }

  nextBtn.classList.remove("hidden");
}

function showPopup(message, color) {
  popup.textContent = message;
  popup.style.color = color;
  popup.classList.remove("hidden");
  popup.classList.add("fade");
  setTimeout(() => {
    popup.classList.remove("fade");
  }, 600);
}

nextBtn.onclick = () => {
  currentIndex++;
  if (currentIndex < questions.length) {
    showQuestion();
  } else {
    showResult();
  }
};

function showResult() {
  questionElem.textContent = `お疲れ様！あなたの得点は ${score} / ${questions.length} 点です！`;
  choicesElem.innerHTML = "";
  popup.classList.add("hidden");
  nextBtn.textContent = "もう一度遊ぶ";
  nextBtn.onclick = () => startQuiz();
}

startQuiz();
